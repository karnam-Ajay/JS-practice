// while

// print odd numbers till 20.
let index = 1;
while (index < 20) {
   // console.log(`the value of index is ${index}`);
    index = index + 2;
}

// arrays with while loop

let myArr = ["Ajay","Vijay","Soham"];

let arrIndex = 0;
while (arrIndex < myArr.length) {
   // console.log(`the values of arr is ${myArr[arrIndex]}`);
    arrIndex = arrIndex + 1;
};


// do-while : it is used where we want run our code atleast once.

let score = 0;

do {
  //  console.log(`the score is ${score}`);
    score = score+1;
} while (score <51);